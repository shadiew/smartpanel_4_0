if (!function_exists('create_default_lang')) {
    function create_default_lang($is_create_file = false, $lang_code = "")
    {
        lang_builder(APPPATH . '../', $data);
        if ($is_create_file && $lang_code != "") {
            $handle = fopen(FCPATH . "app/language/tmp/lang_" . $lang_code . ".txt", "w");
            fwrite($handle, json_encode($data, JSON_PRETTY_PRINT));
            fclose($handle);
        }
        return $data;
    }
}

