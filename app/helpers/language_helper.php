if (!function_exists('get_lang_code_defaut')) {
    function get_lang_code_defaut()
    {

        if (!session('langCurrent')) {
            $CI = &get_instance();

            if (empty($CI->help_model)) {
                $CI->load->model('model', 'help_model');
            }
            $check_lang = $CI->help_model->get('*', LANGUAGE_LIST, "is_default = 1 AND status = 1");
            if (!empty($check_lang)) {
                set_session('langCurrent', $check_lang);
            } else {
                $langDefault = array(
                    'ids' => ids(),
                    'code' => 'en',
                    'country_code' => 'GB',
                    'is_default' => 1,
                    'status' => 1,
                    'created' => NOW,
                );
                $CI->db->insert(LANGUAGE_LIST, $langDefault);
                set_session('langCurrent', (object) $langDefault);
            }
        }
        // Create lang file if it doesn't exists
        if (session('langCurrent')) {
            $current_lang = session('langCurrent');
            $lang_path = APPPATH . "./language/data/" . $current_lang->code . "_lang.php";
            if (!file_exists($lang_path)) {

                lang_builder(APPPATH . '../', $data);
                create_lang_file($lang_path, $data);
            }
        }
        return session('langCurrent');
    }
}

if (!function_exists('lang')) {
    function lang($slug)
    {
        $slug = strtolower($slug);
        $CI = &get_instance();
        if (!session('langCurrent')) {
            get_lang_code_defaut();
        }
        $current_lang = session('langCurrent');
        $result = "";

        $lang_path = APPPATH . "./language/data/" . $current_lang->code . "_lang.php";
        if (file_exists($lang_path)) {
            $CI->load->helper('language');
            $data = $CI->lang->load($current_lang->code, 'data');
            $result = stripslashes($CI->lang->line($slug));
        }
        if ($result == "") {
            $result = ucfirst(str_replace("_", " ", $slug));
        }
        return trim($result);
    }
}

if (!function_exists('get_lang_files')) {
    function get_lang_files($path = "")
    {
        $result = [];
        if ($path != "") {
            $dir = new DirectoryIterator($path);
            foreach ($dir as $fileinfo) {
                if (strrpos($fileinfo->getFilename(), '_lang.php')) {
                    $lang_name = explode('_lang.php', $fileinfo->getFilename());
                    $result[] = $lang_name[0];
                }
            }
        } else {
            $result[] = 'common';
        }
        return $result;
    }
}

/*Build & Load Language*/
if (!function_exists('lang_builder')) {
    function lang_builder($dir, &$data)
    {
        $ffs = scandir($dir, 1);
        unset($ffs[array_search('.', $ffs, true)]);
        unset($ffs[array_search('..', $ffs, true)]);

        // prevent empty ordered elements
        if (count($ffs) < 1) {
            return;
        }

        foreach ($ffs as $ff) {
            if (stripos($ff, "_lang.php")) {
                include $dir . "/" . $ff;
                foreach ($lang as $key => $text) {
                    if ($text != "") {
                        $data[strtolower($key)] = $text;
                    }
                }
            }

            if (!stripos($dir, "system") &&
                !stripos($dir, "assets") &&
                !stripos($dir, "install") &&
                !stripos($dir, "libraries") &&
                !stripos($dir, "git") &&
                !stripos($dir, "modules") &&
                !stripos($dir, "third_party") &&
                !stripos($dir, "vendor") &&
                !stripos($dir, "views") &&
                !stripos($dir, "config")
                ) {
                if (is_dir($dir . '/' . $ff)) {
                    lang_builder($dir . '/' . $ff, $data);
                }

            }
        }
    }
}

